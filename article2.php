<h5>1.楽天競馬登録してみた</h5>
<p>登録に必要なものはたった２つでした！</p>
<ul>
<li>楽天アカウント</li>
<li>楽天銀行口座</li>
</ul>
<p>楽天商圏で生活する民なら余裕でクリアできますね！</p>
<p>準備ができたらサイトへGO！</p>
<p>    →→→<a href="https://keiba.rakuten.co.jp/">https://keiba.rakuten.co.jp/</a></p>
<p>アプリは、ないんですよねー意外なことに。すぐ作りそうですけど。</p>
<p> </p>
<p>楽天アカウントでログインして、暗証番号とニックネームだけ決めます。</p>
<p>その後楽天銀行口座との連携手続きがありますが、口座情報は楽天銀行アプリですぐに確認できるのでわざわざカード引っ張り出したせずに登録完了！</p>
<p>所要時間は５分でした！（うまのはニックネーム考えてプラス５分使いました）</p>
<p> </p>
<p>お昼休憩時間にふと思いついて、お昼休憩中に登録、馬券購入までできましたからね！手軽で最高です！</p>
